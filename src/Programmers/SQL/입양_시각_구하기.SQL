WITH RECURSIVE TIME_TABLE AS (
SELECT 0 AS HOUR

UNION ALL

SELECT HOUR + 1 FROM TIME_TABLE
WHERE HOUR < 23)

SELECT T.HOUR, COUNT(O.ANIMAL_ID) AS COUNT FROM ANIMAL_OUTS O
RIGHT JOIN TIME_TABLE T ON HOUR(O.DATETIME) = T.HOUR
GROUP BY T.HOUR
ORDER BY T.HOUR