--HAVING 사용 전  WITH로 골라내기
WITH FISH_INFO_NN AS (SELECT FISH_TYPE,
                      AVG(IFNULL(LENGTH, 10)) AS AVG_LENGTH
                      FROM FISH_INFO
                      GROUP BY FISH_TYPE),
CHOICE AS (SELECT * FROM FISH_INFO_NN WHERE AVG_LENGTH >= 33)

SELECT COUNT(FISH_TYPE) AS FISH_COUNT, MAX(LENGTH) AS MAX_LENGTH, FISH_TYPE
FROM FISH_INFO
WHERE FISH_TYPE IN (SELECT FISH_TYPE FROM CHOICE)
GROUP BY FISH_TYPE
ORDER BY FISH_TYPE

--HAVING 적용
SELECT COUNT(FISH_TYPE) AS FISH_COUNT, MAX(LENGTH) AS MAX_LENGTH, FISH_TYPE
FROM FISH_INFO
GROUP BY FISH_TYPE
HAVING AVG(IFNULL(LENGTH, 10)) >= 33
ORDER BY FISH_TYPE