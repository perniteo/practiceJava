-- RECURSIVE 는 결과적으로 필요없게 됐다

WITH RECURSIVE
COUNT_USERS AS (SELECT USER_ID
                FROM USER_INFO WHERE YEAR(JOINED) = 2021),
MONTHS AS (SELECT 1 AS MONTH
            UNION ALL
            SELECT MONTH + 1 FROM MONTHS
            WHERE MONTH < 12)

SELECT YEAR(S.SALES_DATE) AS YEAR, MONTH(S.SALES_DATE) AS MONTH,
COUNT(DISTINCT S.USER_ID) AS PURCHASED_USERS,
ROUND(COUNT(DISTINCT S.USER_ID) / (SELECT COUNT(*) FROM COUNT_USERS), 1)
AS PURCHASED_RATIO
FROM ONLINE_SALE S
WHERE S.USER_ID IN (SELECT * FROM COUNT_USERS)
GROUP BY YEAR, MONTH
ORDER BY YEAR, MONTH

