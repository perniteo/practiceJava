-- 처음 시도한 쿼리 / 3세대의 ECOLI를 찾는 건데 반복하는 함수를 몰라서 여러번 하드 쿼리함
-- 반복해서 처리해주는 기능은 없나? 찾아보고 RECURSIVE를 찾게 됨

WITH FIRST_ECOLI AS (SELECT ID FROM ECOLI_DATA WHERE PARENT_ID IS NULL)
,SECOND_ECOLI AS (SELECT ID FROM ECOLI_DATA
                        WHERE PARENT_ID IN (SELECT ID FROM FIRST_ECOLI))

SELECT ID FROM ECOLI_DATA
WHERE PARENT_ID IN (SELECT ID FROM SECOND_ECOLI)
ORDER BY ID

-- RECURSIVE 적용

WITH RECURSIVE ECOLI_GEN AS (
SELECT ID, 1 AS GEN
    FROM ECOLI_DATA WHERE PARENT_ID IS NULL

UNION ALL

SELECT D.ID, G.GEN + 1 FROM ECOLI_DATA D
JOIN ECOLI_GEN G ON D.PARENT_ID = G.ID)

SELECT ID FROM ECOLI_GEN WHERE GEN = 3
ORDER BY ID