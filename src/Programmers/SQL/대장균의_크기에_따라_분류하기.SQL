WITH ECOLI_PERCENT AS (
    SELECT ID, PERCENT_RANK() OVER (ORDER BY SIZE_OF_COLONY DESC) * 100 AS PERCENT FROM ECOLI_DATA ORDER BY ID)

SELECT ID,
CASE WHEN PERCENT <= 25 THEN 'CRITICAL'
WHEN PERCENT <= 50 THEN 'HIGH'
WHEN PERCENT <= 75 THEN 'MEDIUM'
WHEN PERCENT <= 100 THEN 'LOW'
END AS COLONY_NAME
FROM ECOLI_PERCENT
ORDER BY ID