-- & 를 활용한 비트마스크
-- 집계 함수를 사용하지 않았으니 중복제거는 DISTINCT

SELECT DISTINCT D.ID, D.EMAIL, D.FIRST_NAME, D.LAST_NAME FROM SKILLCODES S
JOIN DEVELOPERS D ON D.SKILL_CODE & S.CODE
WHERE S.NAME IN ('C#', 'Python')
-- GROUP BY D.ID, D.EMAIL, D.FIRST_NAME, D.LAST_NAME
ORDER BY D.ID


--- FrontEnd 개발자 찾기
